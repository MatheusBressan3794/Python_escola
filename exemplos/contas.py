n1 = float(input('Digite um número: '))
n2 = float(input('Digite outro número: '))
soma = n1 + n2
sub = n1 - n2
div = n1 / n2
mut = n1 * n2

print('A soma dos dois é {}'.format(soma))
print('A subtração é {}'.format(sub))
print('A divisão deles é {}'.format(div))
print('A mutiplicação é {}'.format(mut))